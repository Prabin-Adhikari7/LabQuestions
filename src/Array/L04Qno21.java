package Array;

public class L04Qno21 {

	public static void main(String[] args) {
		int []aa=new int [] {2,3,4,5,55,6};
		for(int i=0; i<aa.length; i++) {
			if(aa[i]%2==0) {
				
				System.out.println(aa[i]+"= even ");
			}
			else {
				System.out.println(aa[i]+"= odd");
			}
		}
		
//		21.	Write a Java Program to find the number of even and odd integers in a given array of integers. 
	}

}
